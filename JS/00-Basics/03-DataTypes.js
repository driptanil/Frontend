// numbers
const num = 5;
console.log(typeof(num));

// strings
const str = "String";
console.log(typeof(str));

// booleans
const bool = true;
console.log(typeof(bool));

// undefined 
// -> const has to be initialized with a value
let notDefined;
console.log(typeof(notDefined));

// null
const nothing = null;
console.log(typeof(nothing));

// symbols
const sign = Symbol();
console.log(typeof(sign));